package by.training;

public class Arithmetic {
    public double add(double a, double b) {
        return a + b;
    }

    public double deduct(double a, double b) {
        return a - b;
    }

    public double div(double a, double b) {
        return a / b;
    }

    public double mult(double a, double b) {
        return a * b;
    }
}
